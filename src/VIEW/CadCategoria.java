    public void incluir() {
        if (!txtNome.getText().trim().isEmpty() && cbTipo.getSelectedIndex() > 0) {
            Entidade.Categoria categoria = new Entidade.Categoria(),
                    cNome;
            cNome = new CategoriaDAO().consultarPorNomeUk(txtNome.getText().trim());
            if (cNome != null) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse nome já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            categoria.setNomeCategoria(txtNome.getText().trim());
            categoria.setTipo(cbTipo.getSelectedItem().toString());
            if (cbTipo.getSelectedItem().toString().equals("Bebida")) {
                categoria.setControleVenda(chControle.isSelected());
            }
            try {
                if (JOptionPane.showConfirmDialog((Frame) getParent(), "Deseja realmente incluir a "
                        + "categoria?", "Incluir", 0) == 0) {
                    new CategoriaDAO().incluir(categoria);
                    JOptionPane.showMessageDialog((Frame) getParent(), "Categoria incluída com sucesso!");
                    categoriaSelecionada = null;
                    limpar();
                    btAlterar.setEnabled(false);
                    txtNome.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível incluir a categoria!\n"
                        + "<html><b>" + e.getClass() + "</html></b>"
                        + "\nContate o desenvolvedor!", "Erro ao incluir", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (txtNome.getText().trim().isEmpty()) {
                txtNome.setBackground(new java.awt.Color(255, 155, 155));
            }
            if (cbTipo.getSelectedIndex() <= 0) {
                cbTipo.setBackground(new java.awt.Color(255, 155, 155));
            }
            JOptionPane.showMessageDialog((Frame) getParent(), "Preencha o(s) campo(s) em vermelho!");
        }
    }

    public void consultar() {
        if (!txtConsulta.getText().isEmpty()) {
            CategoriaDAO categoriaDAO = new CategoriaDAO();
            String busca = txtConsulta.getText();
            setModeloTabela();
            List<Entidade.Categoria> categorias = new ArrayList<>();
            if (cbFiltro.getSelectedIndex() == 0) {
                try {
                    Entidade.Categoria c = categoriaDAO.consultarPorCodigo(Integer.parseInt(busca));
                    if (c != null) {
                        categorias.add(c);
                    }
                } catch (NumberFormatException erro) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Digite apenas números para o Código!");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a busca!");
                }
            } else if (cbFiltro.getSelectedIndex() == 1) {
                try {
                    categorias = categoriaDAO.consultarPorNome(busca);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a busca!");
                }
            } else {
                try {
                    categorias = categoriaDAO.consultarPorTipo(busca);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a busca!");
                }
            }
            carregarTabela(categorias);
        } else {
            setModeloTabela();
        }
    }

    public void alterar() {
        if (!txtNome.getText().trim().isEmpty() && cbTipo.getSelectedIndex() > 0) {
            Entidade.Categoria cNome;
            cNome = new CategoriaDAO().consultarPorNomeUk(txtNome.getText().trim());
            if (cNome != null && !categoriaSelecionada.getNomeCategoria().equals(txtNome.getText().trim())) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse nome já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            categoriaSelecionada.setNomeCategoria(txtNome.getText().trim());
            categoriaSelecionada.setTipo(cbTipo.getSelectedItem().toString());
            if (cbTipo.getSelectedItem().toString().equals("Bebida")) {
                categoriaSelecionada.setControleVenda(chControle.isSelected());
            }
            try {
                if (JOptionPane.showConfirmDialog((Frame) getParent(), "Deseja realmente alterar a "
                        + "categoria?", "Alterar", 0) == 0) {
                    new CategoriaDAO().alterar(categoriaSelecionada);
                    JOptionPane.showMessageDialog((Frame) getParent(), "Categoria alterada com sucesso!");
                    categoriaSelecionada = null;
                    limpar();
                    btAlterar.setEnabled(false);
                    txtNome.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível alterar a categoria!\n"
                        + "<html><b>" + e.getClass() + "</html></b>"
                        + "\nContate o desenvolvedor!", "Erro ao alterar", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (txtNome.getText().trim().isEmpty()) {
                txtNome.setBackground(new java.awt.Color(255, 155, 155));
            }
            if (cbTipo.getSelectedIndex() <= 0) {
                cbTipo.setBackground(new java.awt.Color(255, 155, 155));
            }
            JOptionPane.showMessageDialog((Frame) getParent(), "Preencha o(s) campo(s) em vermelho!");
        }
    }

    private void setModeloTabela() {
        String[] colunas = new String[]{"Código", "Nome", "Tipo", "Controle de venda"};

        DefaultTableModel modelo = new DefaultTableModel(null, colunas) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            @Override
            public boolean isCellEditable(int row, int columnIndex) {
                return canEdit[columnIndex];
            }

        };
        tabelaCategoria.setModel(modelo);
        tabelaCategoria.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabelaCategoria.getColumnModel().getColumn(0).setPreferredWidth(73);
        tabelaCategoria.getColumnModel().getColumn(1).setPreferredWidth(200);
        tabelaCategoria.getColumnModel().getColumn(2).setPreferredWidth(90);
        tabelaCategoria.getColumnModel().getColumn(3).setPreferredWidth(118);
        tabelaCategoria.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void carregarTabela(List<Entidade.Categoria> categorias) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaCategoria.getModel();
        for (Entidade.Categoria c : categorias) {
            String controle = "-";
            if (c.getControleVenda() != null) {
                if (c.getControleVenda()) {
                    controle = "Sim";
                } else {
                    controle = "Não";
                }
            }
            Object[] linha = new Object[]{
                c.getPkCategoria(),
                c.getNomeCategoria(),
                c.getTipo(),
                controle
            };
            modelo.addRow(linha);
        }
    }

    private void limpar() {
        try {
            txtNome.getDocument().remove(0, txtNome.getDocument().getLength());
        } catch (BadLocationException ex) {
            Logger.getLogger(CadCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }

        cbTipo.setSelectedIndex(0);
        chControle.setSelected(false);

        categoriaSelecionada = null;
        txtNome.setBackground(new java.awt.Color(255, 255, 255));
        cbTipo.setBackground(new java.awt.Color(255, 255, 255));
    }

