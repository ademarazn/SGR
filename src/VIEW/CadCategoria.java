    private void btSelecionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSelecionarMouseClicked
        if (tabelaCategoria.getSelectedRow() != -1) {
            limpar();
            int linha = tabelaCategoria.getSelectedRow();
            categoriaSelecionada = (Categoria) new CategoriaDAO()
                    .consultarPorCodigo(Integer
                            .parseInt(tabelaCategoria
                                    .getModel()
                                    .getValueAt(linha, 0)
                                    .toString()));
            ((AbstractDocument) txtNome.getDocument()).setDocumentFilter(new DocumentFilter());
            txtNome.setText(tabelaCategoria.getModel().getValueAt(linha, 1).toString());
            ((AbstractDocument) txtNome.getDocument()).setDocumentFilter(new FixedLengthDocument(50, txtNome));

            cbTipo.setSelectedItem(tabelaCategoria.getModel().getValueAt(linha, 2).toString());

            if (!tabelaCategoria.getModel().getValueAt(linha, 3).toString().equals("-")) {
                if (tabelaCategoria.getModel().getValueAt(linha, 3).toString().equals("Sim")) {
                    chControle.setSelected(true);
                } else {
                    chControle.setSelected(false);
                }
            }

            btAlterar.setEnabled(true);
            consultar.dispose();
        } else {
            JOptionPane.showMessageDialog((Frame) getParent(), "Selecione uma categoria na tabela"
                    + " antes de pressionar o botão!",
                    "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btSelecionarMouseClicked

