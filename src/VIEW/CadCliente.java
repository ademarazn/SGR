    public void incluir() {
        if (!txtNome.getText().trim().isEmpty() && !txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty()
                && !txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty() && dateNascimento.getDateEditor().getDate() != null) {
            ClienteDAO clienteDAO = new ClienteDAO();
            Entidade.Cliente cliente = new Entidade.Cliente(),
                    cCpf, cRg;
            cCpf = clienteDAO.consultarPorCpf(txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").trim());
            if (cCpf != null) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse CPF já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            cRg = clienteDAO.consultarPorRg(txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").trim());
            if (cRg != null) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse RG já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            cliente.setNomeCliente(txtNome.getText().trim());
            cliente.setCpf(txtCPF.getText().replace(".", "").replace("-", ""));
            cliente.setRg(txtRG.getText().replace(".", "").replace("-", ""));
            cliente.setDtNasc(dateNascimento.getDate());
            cliente.setTelefone(txtTelefone.getText().replace("(", "").replace(")", "").replace("-", "").replace(" ", ""));
            cliente.setCelular(txtCelular.getText().replace("(", "").replace(")", "").replace("-", "").replace(" ", ""));
            cliente.setEmail(txtEmail.getText());
            cliente.setEndereco(txtEndereco.getText());
            cliente.setNum(txtNumero.getText());
            cliente.setBairro(txtBairro.getText());
            cliente.setComplemento(txtComplemento.getText());
            cliente.setCidade(txtCidade.getText());
            cliente.setCep(txtCep.getText().replace("-", ""));
            if (cbEstado.getSelectedIndex() > 0) {
                cliente.setEstado(cbEstado.getSelectedItem().toString());
            }
            try {
                if (JOptionPane.showConfirmDialog((Frame) getParent(), "Deseja realmente incluir o "
                        + "cliente?", "Incluir", 0) == 0) {
                    clienteDAO.incluir(cliente);
                    JOptionPane.showMessageDialog((Frame) getParent(), "Cliente incluído com sucesso!");
                    clienteSelecionado = null;
                    limpar();
                    btAlterar.setEnabled(false);
                    txtNome.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível incluir o cliente!\n"
                        + "<html><b>" + e.getClass() + "</html></b>"
                        + "\nContate o desenvolvedor!", "Erro ao incluir", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            verificaCamposVazios();
        }
    }

    public void consultar() {
        if (!txtConsulta.getText().isEmpty()) {
            ClienteDAO clienteDAO = new ClienteDAO();
            String busca = txtConsulta.getText();
            setModeloTabela();
            tabelaCliente.getParent().setBackground(new java.awt.Color(233, 232, 230));
            List<Entidade.Cliente> clientes = new ArrayList<>();
            if (cbFiltro.getSelectedIndex() == 0) {
                try {
                    Entidade.Cliente c = clienteDAO.consultarPorCodigo(Integer.parseInt(busca));
                    if (c != null) {
                        clientes.add(c);
                    }
                } catch (NumberFormatException erro) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Digite apenas números para o Código!");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a busca!");
                }
            } else if (cbFiltro.getSelectedIndex() == 1) {
                try {
                    clientes = clienteDAO.consultarPorNome(busca);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a busca!");
                }
            } else {
                try {
                    Entidade.Cliente c = clienteDAO.consultarPorCpf(busca);
                    if (c != null) {
                        clientes.add(c);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a busca!");
                }
            }
            carregarTabela(clientes);
        } else {
            setModeloTabela();
        }
    }

