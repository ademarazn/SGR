    private void verificaCamposVazios() {
        if (txtNome.getText().trim().isEmpty()) {
            txtNome.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty()) {
            txtCPF.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty()) {
            txtRG.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (dateNascimento.getDateEditor().getDate() == null) {
            dateNascimento.getDateEditor().getUiComponent().setBackground(new java.awt.Color(255, 155, 155));
        }
        JOptionPane.showMessageDialog((Frame) getParent(), "Preencha o(s) campo(s) em vermelho!");
    }

