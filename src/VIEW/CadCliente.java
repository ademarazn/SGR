    private void carregarTabela(List<Entidade.Cliente> clientes) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaCliente.getModel();
        for (Entidade.Cliente c : clientes) {
            Object[] linha = new Object[]{
                c.getPkCliente(),
                c.getNomeCliente(),
                c.getCpf(),
                c.getRg(),
                c.getDtNasc(),
                c.getTelefone(),
                c.getCelular(),
                c.getEmail(),
                c.getEndereco(),
                c.getNum(),
                c.getBairro(),
                c.getComplemento(),
                c.getCidade(),
                c.getEstado(),
                c.getCep()
            };
            modelo.addRow(linha);
        }
    }

