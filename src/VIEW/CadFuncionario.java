    public void ativarDesativar() {
        if (btAtiDesat.isSelected()) {
            if (JOptionPane.showConfirmDialog((Frame) getParent(), "Deseja realmente ativar o "
                    + "funcionário?", "Ativar", 0) == 0) {
                try {
                    funcionarioSelecionado.setStatus(true);
                    new FuncionarioDAO().alterar(funcionarioSelecionado);
                    JOptionPane.showMessageDialog((Frame) getParent(), "Funcionário ativado com sucesso!");
                    cbStatus.setSelectedItem("Ativo");
                    resetFieldColor();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível ativar o funcionário!\n"
                            + "<html><b>" + e.getClass() + "</html></b>"
                            + "\nContate o desenvolvedor!", "Erro ao ativar", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            if (JOptionPane.showConfirmDialog((Frame) getParent(), "Deseja realmente desativar o "
                    + "funcionário?", "Desativar", 0) == 0) {
                try {
                    funcionarioSelecionado.setStatus(false);
                    new FuncionarioDAO().alterar(funcionarioSelecionado);
                    JOptionPane.showMessageDialog((Frame) getParent(), "Funcionário desativado com sucesso!");
                    cbStatus.setSelectedItem("Inativo");
                    resetFieldColor();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível desativar o funcionário!\n"
                            + "<html><b>" + e.getClass() + "</html></b>"
                            + "\nContate o desenvolvedor!", "Erro ao desativar", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        verificarStatus();
    }

    private void setModeloTabela() {
        String[] colunas = new String[]{"Código", "Nome", "CPF", "RG",
            "Cargo", "Salário", "Dt. Contratação", "Status", "Login", "Senha"};
        DefaultTableModel modelo = new DefaultTableModel(null, colunas) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false,
                false, false, false, false, false
            };

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }

            @Override
            public Class getColumnClass(int col) {
                if (col == 6) {
                    return java.util.Date.class;
                } else {
                    return String.class;
                }
            }

        };
        tabelaFuncionario.setModel(modelo);
        tabelaFuncionario.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabelaFuncionario.getColumnModel().getColumn(0).setPreferredWidth(73);
        tabelaFuncionario.getColumnModel().getColumn(1).setPreferredWidth(200);
        tabelaFuncionario.getColumnModel().getColumn(2).setPreferredWidth(90);
        tabelaFuncionario.getColumnModel().getColumn(3).setPreferredWidth(90);
        tabelaFuncionario.getColumnModel().getColumn(4).setPreferredWidth(90);
        tabelaFuncionario.getColumnModel().getColumn(5).setPreferredWidth(90);
        tabelaFuncionario.getColumnModel().getColumn(6).setPreferredWidth(100);
        tabelaFuncionario.getColumnModel().getColumn(7).setPreferredWidth(90);
        tabelaFuncionario.getColumnModel().getColumn(8).setPreferredWidth(90);
        tabelaFuncionario.getColumnModel().getColumn(9).setPreferredWidth(90);
        tabelaFuncionario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void carregarTabela(List<Funcionario> funcionarios) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaFuncionario.getModel();
        for (Funcionario f : funcionarios) {
            String salario = null;
            if (f.getSalario() != null) {
                salario = f.getSalario().toString().replace('.', ',');
            }
            Object[] linha = new Object[]{
                f.getPkFuncionario(),
                f.getNomeFunc(),
                f.getCpf(),
                f.getRg(),
                f.getCargo(),
                salario,
                f.getDtContratacao(),
                f.getStatus() ? "Ativo" : "Inativo",
                f.getLogin(),
                f.getSenha()
            };
            modelo.addRow(linha);
        }
    }

    private void verificarStatus() {
        if (cbStatus.getSelectedItem().toString().equals("Ativo")) {
            btAtiDesat.setSelected(false);
            btAtiDesat.setText("Desativar");
            btAtiDesat.setIcon(new javax.swing.ImageIcon(getClass().getResource("/ICON/ic_visibility_off_18pt.png")));
        } else {
            btAtiDesat.setSelected(true);
            btAtiDesat.setText("Ativar");
            btAtiDesat.setIcon(new javax.swing.ImageIcon(getClass().getResource("/ICON/ic_visibility_18pt.png")));
        }
    }

    private void limpar() {
        try {
            txtNome.getDocument().remove(0, txtNome.getDocument().getLength());
            txtCPF.setValue(null);
            txtCPF.setText(null);
            txtRG.setValue(null);
            txtRG.setText(null);
            txtLogin.getDocument().remove(0, txtLogin.getDocument().getLength());
            txtSenha.getDocument().remove(0, txtSenha.getDocument().getLength());
        } catch (BadLocationException ex) {
            Logger.getLogger(CadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtSalario.setValue(null);
        txtSalario.setText(null);
        cbCargo.setSelectedIndex(0);
        cbStatus.setSelectedIndex(0);
        dateContrat.setDate(null);
        funcionarioSelecionado = null;
        resetFieldColor();
    }

