    private void verificarCamposVazios() {
        if (txtNome.getText().trim().isEmpty()) {
            txtNome.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty()) {
            txtCPF.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty()) {
            txtRG.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (cbCargo.getSelectedIndex() <= 0) {
            cbCargo.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (dateContrat.getDateEditor().getDate() == null) {
            dateContrat.getDateEditor().getUiComponent().setBackground(new java.awt.Color(255, 155, 155));
        }
        if (txtLogin.getText().trim().isEmpty()) {
            txtLogin.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (txtSenha.getText().isEmpty()) {
            txtSenha.setBackground(new java.awt.Color(255, 155, 155));
        }
        if (cbStatus.getSelectedIndex() <= 0) {
            cbStatus.setBackground(new java.awt.Color(255, 155, 155));
        }
        JOptionPane.showMessageDialog((Frame) getParent(), "Preencha o(s) campo(s) em vermelho!");
    }

