    public void incluir() {
        if (!txtNome.getText().trim().isEmpty() && !txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty()
                && !txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").isEmpty() && cbCargo.getSelectedIndex() > 0
                && dateContrat.getDateEditor().getDate() != null && !txtLogin.getText().trim().isEmpty()
                && !txtSenha.getText().isEmpty() && cbStatus.getSelectedIndex() > 0) {
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
            Funcionario funcionario = new Funcionario(),
                    fCpf, fRg, fLogin;
            fCpf = funcionarioDAO.consultarPorCpf(txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").trim());
            if (fCpf != null) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse CPF já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            fRg = funcionarioDAO.consultarPorRg(txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").trim());
            if (fRg != null) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse RG já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            fLogin = funcionarioDAO.consultarPorLogin(txtLogin.getText().trim());
            if (fLogin != null) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Esse Login já existe no sistema!",
                        "Atenção", JOptionPane.WARNING_MESSAGE);
                return;
            }
            funcionario.setNomeFunc(txtNome.getText().trim());
            funcionario.setCpf(txtCPF.getText().replace(".", "").replace("-", "").replace(" ", "").trim());
            funcionario.setRg(txtRG.getText().replace(".", "").replace("-", "").replace(" ", "").trim());
            funcionario.setLogin(txtLogin.getText().trim());
            funcionario.setSenha(txtSenha.getText());
            funcionario.setDtContratacao(dateContrat.getDate());
            if (!txtSalario.getText().isEmpty()) {
                try {
                    funcionario.setSalario(Float.parseFloat(txtSalario.getText().replace(".", "").replace(',', '.')));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Digite um valor válido para o Salário!", "Salário inválido", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            funcionario.setCargo(cbCargo.getSelectedItem().toString());
            if (cbStatus.getSelectedItem().equals("Ativo")) {
                funcionario.setStatus(true);
            } else {
                funcionario.setStatus(false);
            }
            try {
                if (JOptionPane.showConfirmDialog((Frame) getParent(), "Deseja realmente incluir o "
                        + "funcionário?", "Incluir", 0) == 0) {
                    funcionarioDAO.incluir(funcionario);
                    JOptionPane.showMessageDialog((Frame) getParent(), "Funcionário incluído com sucesso!");
                    funcionarioSelecionado = null;
                    limpar();
                    btAlterar.setEnabled(false);
                    btAtiDesat.setEnabled(false);
                    txtNome.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível incluir o funcionário!\n"
                        + "<html><b>" + e.getClass() + "</html></b>"
                        + "\nContate o desenvolvedor!", "Erro ao incluir", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            verificarCamposVazios();
        }
    }

    public void consultar() {
        if (!txtConsulta.getText().isEmpty()) {
            FuncionarioDAO funcionarioDAO = new FuncionarioDAO();
            String busca = txtConsulta.getText();
            setModeloTabela();
            List<Funcionario> funcionarios = new ArrayList<>();
            if (cbFiltro.getSelectedIndex() == 0) {
                try {
                    funcionarios = funcionarioDAO.consultarPorNome(busca);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a consulta!");
                }
            } else {
                try {
                    Funcionario f = funcionarioDAO.consultarPorCpf(busca);
                    if (f != null) {
                        funcionarios.add(f);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog((Frame) getParent(), "Não foi possível realizar a consulta!");
                }
            }
            carregarTabela(funcionarios);
        } else {
            setModeloTabela();
        }
    }

