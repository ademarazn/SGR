    private void btSelecionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSelecionarMouseClicked
        if (tabelaFuncionario.getSelectedRow() != -1) {
            limpar();
            int linha = tabelaFuncionario.getSelectedRow();
            funcionarioSelecionado = (Funcionario) new FuncionarioDAO()
                    .consultarPorCpf(tabelaFuncionario
                            .getModel()
                            .getValueAt(linha, 2)
                            .toString());

            ((AbstractDocument) txtNome.getDocument()).setDocumentFilter(new DocumentFilter());
            txtNome.setText(tabelaFuncionario.getModel().getValueAt(linha, 1).toString());
            ((AbstractDocument) txtNome.getDocument()).setDocumentFilter(new FixedLengthDocument(50, txtNome));

            txtCPF.setText(tabelaFuncionario.getModel().getValueAt(linha, 2).toString());
            txtRG.setText(tabelaFuncionario.getModel().getValueAt(linha, 3).toString());
            try {
                txtCPF.commitEdit();
                txtRG.commitEdit();
            } catch (ParseException ex) {
                Logger.getLogger(CadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }


            cbCargo.setSelectedItem(tabelaFuncionario.getModel().getValueAt(linha, 4).toString());
            if (tabelaFuncionario.getModel().getValueAt(linha, 5) != null) {
                txtSalario.setValue(Float.valueOf(tabelaFuncionario.getModel().getValueAt(linha, 5).toString().replace(",", ".")));
            } else {
                txtSalario.setValue(null);
            }
            Date dt;
            try {
                dt = new SimpleDateFormat("yyyy-MM-dd")
                        .parse(tabelaFuncionario.getModel().getValueAt(linha, 6).toString());
                dateContrat.setDate(dt);
            } catch (ParseException ex) {
                Logger.getLogger(CadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
            cbStatus.setSelectedItem(tabelaFuncionario.getModel().getValueAt(linha, 7).toString());

            ((AbstractDocument) txtLogin.getDocument()).setDocumentFilter(new DocumentFilter());
            txtLogin.setText(tabelaFuncionario.getModel().getValueAt(linha, 8).toString());
            ((AbstractDocument) txtLogin.getDocument()).setDocumentFilter(new FixedLengthDocument(16, txtLogin));

            ((AbstractDocument) txtSenha.getDocument()).setDocumentFilter(new DocumentFilter());
            txtSenha.setText(tabelaFuncionario.getModel().getValueAt(linha, 9).toString());
            ((AbstractDocument) txtSenha.getDocument()).setDocumentFilter(new FixedLengthDocument(16, txtSenha));

            btAlterar.setEnabled(true);
            btAtiDesat.setEnabled(true);
            verificarStatus();
            consultar.dispose();
        } else {
            JOptionPane.showMessageDialog((Frame) getParent(), "Selecione um funcionário na tabela"
                    + " antes de pressionar o botão!",
                    "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btSelecionarMouseClicked

